namespace Lib.Geometry;

/// <summary>
/// Rotation direction for turning operations.
/// </summary>
public enum Rotation
{
    /// <summary>
    /// No rotation.
    /// </summary>
    None = 0,
    /// <summary>
    /// Clockwise rotation.
    /// </summary>
    Clockwise = 1,
    /// <summary>
    /// Counter-clockwise rotation.
    /// </summary>
    CounterClockwise = 2,
}
